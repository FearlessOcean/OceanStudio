# ── 语义嵌入 RAG ──────────────────────────────────────────────────────────────

_embed_model = None          # SentenceTransformer 实例
_knowledge_vecs = None       # shape: (N, D)，预编码的知识条目向量
_embed_lock = threading.Lock()
_embed_ready = False
_EMBED_MODEL_NAME = "BAAI/bge-small-zh-v1.5"
_SIM_THRESHOLD = 0.30        # 余弦相似度最低阈值


def _load_embed_model() -> bool:
    """尝试加载语义嵌入模型并预编码知识库，失败则静默返回 False。"""
    global _embed_model, _knowledge_vecs, _embed_ready
    with _embed_lock:
        if _embed_ready:
            return _embed_model is not None
        try:
            import numpy as np
            from sentence_transformers import SentenceTransformer
            print(f"[RAG] 加载嵌入模型 {_EMBED_MODEL_NAME} ...")
            model = SentenceTransformer(_EMBED_MODEL_NAME)
            texts = [content for _, content in _KNOWLEDGE]
            vecs = model.encode(texts, normalize_embeddings=True, show_progress_bar=False)
            _embed_model = model
            _knowledge_vecs = np.array(vecs, dtype="float32")
            _embed_ready = True
            print(f"[RAG] 嵌入模型就绪，已编码 {len(texts)} 条知识条目（{_knowledge_vecs.nbytes//1024} KB）")
            return True
        except Exception as e:
            print(f"[RAG] 嵌入模型不可用（{e.__class__.__name__}），降级至关键词检索")
            _embed_ready = True
            return False


def warmup_rag():
    """在后台线程预热 RAG（供 main.py 在启动时并行调用）。"""
    t = threading.Thread(target=_load_embed_model, name="rag-warmup", daemon=True)
    t.start()
    return t


def _retrieve_semantic(query: str, top_k: int) -> str:
    import numpy as np
    q_vec = _embed_model.encode([query], normalize_embeddings=True)
    scores = (_knowledge_vecs @ q_vec.T).squeeze()
    top_idx = scores.argsort()[::-1][:top_k]
    results = []
    for i in top_idx:
        if float(scores[i]) >= _SIM_THRESHOLD:
            results.append(f"[参考知识] {_KNOWLEDGE[i][1]}")
    return "\n\n".join(results)


def _retrieve_keyword(query: str, top_k: int) -> str:
    scored: list[tuple[int, str]] = []
    for keywords, content in _KNOWLEDGE:
        score = sum(1 for kw in keywords if kw in query)
        if score > 0:
            scored.append((score, content))
    scored.sort(key=lambda x: x[0], reverse=True)
    return "\n\n".join(f"[参考知识] {c}" for _, c in scored[:top_k])


def retrieve(query: str, top_k: int = 2) -> str:
    """语义嵌入检索；模型不可用时降级至关键词检索。"""
    if not _embed_ready:
        _load_embed_model()
    if _embed_model is not None and _knowledge_vecs is not None:
        return _retrieve_semantic(query, top_k)
    return _retrieve_keyword(query, top_k)
